fun main(){
    val n: Int = 10
    val d: Double = n.toDouble()
    println("Integer value: $n")
    println("Double value: $d")

    val s: String = "10"
    val i: Int = s.toInt()
    println("String value: $s")
    println("Double value (To string): $i")

    val d1: Double = s.toDouble()
    println("Double value (To string): $d1")
}